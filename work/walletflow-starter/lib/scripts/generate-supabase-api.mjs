import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const workspaceRoot = path.resolve(__dirname, '../..');
const generatedTsHeader = `// AUTO-GENERATED FILE. DO NOT EDIT.\n// Generated by: pnpm gen:supabase-api\n\n`;
const generatedJsonMarker = 'AUTO-GENERATED FILE. DO NOT EDIT. Generated by pnpm gen:supabase-api';
const functionsRoots = [
  path.join(workspaceRoot, 'supabase', 'functions'),
  path.join(workspaceRoot, 'supabase', 'function'),
];
const generatedDir = path.join(workspaceRoot, 'lib', 'api-client-react', 'src', 'generated');
const openApiDir = path.join(generatedDir, 'spec');

function toEndpointName(name) {
  return name.replace(/[^a-zA-Z0-9]+(.)/g, (_, chr) => chr.toUpperCase());
}

function parseObjectLiteralShape(block) {
  const entries = [...block.matchAll(/([A-Za-z0-9_]+)\s*:\s*'([^']+)'/g)];
  return Object.fromEntries(entries.map((match) => [match[1], match[2]]));
}

function renderInterface(typeName, shape) {
  const lines = Object.entries(shape).map(([key, value]) => `  ${key}: ${value};`);
  return `export interface ${typeName} {\n${lines.join('\n')}\n}\n`;
}

function hasRequestPayload(contract) {
  return Boolean(contract.requestTypeName) || Object.keys(contract.requestShape).length > 0;
}

async function loadContracts() {
  let functionsRoot = null;
  for (const candidate of functionsRoots) {
    try {
      await readdir(candidate);
      functionsRoot = candidate;
      break;
    } catch {
      continue;
    }
  }

  if (!functionsRoot) return [];

  const entries = await readdir(functionsRoot, { withFileTypes: true });
  const contracts = [];

  for (const entry of entries) {
    if (!entry.isDirectory() || entry.name.startsWith('_')) continue;
    const contractPath = path.join(functionsRoot, entry.name, 'contract.ts');
    let content;
    try {
      content = await readFile(contractPath, 'utf8');
    } catch {
      continue;
    }

    const nameMatch = content.match(/name:\s*'([^']+)'/);
    const methodMatch = content.match(/method:\s*'([^']+)'/);
    const pathMatch = content.match(/path:\s*'([^']+)'/);
    const requestTypeNameMatch = content.match(/requestTypeName:\s*'([^']+)'/);
    const responseTypeNameMatch = content.match(/responseTypeName:\s*'([^']+)'/);
    const requestShapeMatch = content.match(/requestShape:\s*\{([\s\S]*?)\}\s*,/);
    const responseShapeMatch = content.match(/responseShape:\s*\{([\s\S]*?)\}\s*,?\s*\}?/);

    if (!nameMatch || !methodMatch || !pathMatch || !responseTypeNameMatch || !responseShapeMatch) {
      continue;
    }

    contracts.push({
      name: nameMatch[1],
      method: methodMatch[1],
      path: pathMatch[1],
      requestTypeName: requestTypeNameMatch?.[1] ?? null,
      requestShape: requestShapeMatch ? parseObjectLiteralShape(requestShapeMatch[1]) : {},
      responseTypeName: responseTypeNameMatch[1],
      responseShape: parseObjectLiteralShape(responseShapeMatch[1]),
    });
  }

  return contracts;
}

async function main() {
  const contracts = await loadContracts();
  await mkdir(openApiDir, { recursive: true });
  await mkdir(generatedDir, { recursive: true });

  const routeManifest = {
    _generated: generatedJsonMarker,
    version: 1,
    routes: contracts,
  };
  await writeFile(
    path.join(openApiDir, 'route-manifest.json'),
    `${JSON.stringify(routeManifest, null, 2)}\n`
  );

  const openApi = {
    _generated: generatedJsonMarker,
    openapi: '3.1.0',
    info: {
      title: 'Supabase Edge Functions',
      version: '0.0.1',
    },
    paths: Object.fromEntries(
      contracts.map((contract) => [
        contract.path,
        {
          [contract.method.toLowerCase()]: {
            operationId: toEndpointName(contract.name),
            responses: {
              200: {
                description: 'OK',
              },
            },
          },
        },
      ])
    ),
  };
  await writeFile(
    path.join(openApiDir, 'edge-functions.openapi.json'),
    `${JSON.stringify(openApi, null, 2)}\n`
  );

  const endpointLines = contracts.map(
    (contract) =>
      `  ${toEndpointName(contract.name)}: { method: '${contract.method}', path: '${contract.path}', requestType: ${contract.requestTypeName ? `'${contract.requestTypeName}'` : 'null'}, responseType: '${contract.responseTypeName}' },`
  );

  const apiFunctionLines = contracts.map((contract) => {
    const endpointName = toEndpointName(contract.name);
    const fnName = `call${endpointName.charAt(0).toUpperCase()}${endpointName.slice(1)}`;
    const responseType = contract.responseTypeName;
    const requestType = contract.requestTypeName ?? 'Record<string, never>';
    const needsBody = hasRequestPayload(contract);
    const signature = needsBody
      ? `body: ${requestType}, init?: GeneratedFunctionInvokeInit`
      : `init?: GeneratedFunctionInvokeInit`;
    const requestOptions = [
      `functionName: '${contract.name}'`,
      `path: '${contract.path}'`,
      `method: '${contract.method}'`,
      needsBody ? `body` : null,
      `init`,
    ]
      .filter(Boolean)
      .map((line) => `    ${line}`)
      .join(',\n');

    return `export async function ${fnName}(${signature}): Promise<${responseType}> {\n  return invokeGeneratedFunction<${responseType}>({\n${requestOptions}\n  });\n}\n`;
  });

  const hookFunctionLines = contracts.map((contract) => {
    const endpointName = toEndpointName(contract.name);
    const fnName = `call${endpointName.charAt(0).toUpperCase()}${endpointName.slice(1)}`;
    const baseName = `${endpointName.charAt(0).toUpperCase()}${endpointName.slice(1)}`;
    const requestType = contract.requestTypeName ?? 'Record<string, never>';
    const responseType = contract.responseTypeName;
    const needsBody = hasRequestPayload(contract);

    if (contract.method.toUpperCase() === 'GET' && !needsBody) {
      const hookName = `use${baseName}Query`;
      return `export function ${hookName}(options?: Omit<UseQueryOptions<${responseType}, Error>, 'queryKey' | 'queryFn'>) {\n  return useQuery<${responseType}, Error>({\n    queryKey: ['${contract.name}'],\n    queryFn: () => ${fnName}(),\n    ...options,\n  });\n}\n`;
    }

    const hookName = `use${baseName}Mutation`;
    const variablesType = needsBody ? requestType : 'void';
    const mutationFn = needsBody ? `(body) => ${fnName}(body)` : `() => ${fnName}()`;
    return `export function ${hookName}(options?: UseMutationOptions<${responseType}, Error, ${variablesType}>) {\n  return useMutation<${responseType}, Error, ${variablesType}>({\n    mutationKey: ['${contract.name}'],\n    mutationFn: ${mutationFn},\n    ...options,\n  });\n}\n`;
  });

  await writeFile(
    path.join(generatedDir, 'endpoints.ts'),
    `${generatedTsHeader}export const generatedEndpoints = {\n${endpointLines.join('\n')}\n} as const;\n`
  );

  await writeFile(
    path.join(generatedDir, 'types.ts'),
    contracts.length === 0
      ? `${generatedTsHeader}// No generated API types yet. Add contract.ts files beside Supabase functions to enable generation.\nexport {};\n`
      : `${generatedTsHeader}${contracts
          .flatMap((contract) => {
            const parts = [];
            if (contract.requestTypeName) {
              parts.push(renderInterface(contract.requestTypeName, contract.requestShape));
            }
            parts.push(renderInterface(contract.responseTypeName, contract.responseShape));
            return parts;
          })
          .join('\n')}`
  );

  await writeFile(
    path.join(generatedDir, 'api.ts'),
    `${generatedTsHeader}import { useMutation, useQuery, type UseMutationOptions, type UseQueryOptions } from '@tanstack/react-query';\nimport { invokeGeneratedFunction, type GeneratedFunctionInvokeInit } from './runtime';\nimport type {\n${contracts
      .flatMap((contract) => [
        contract.requestTypeName ? `  ${contract.requestTypeName},` : null,
        `  ${contract.responseTypeName},`,
      ])
      .filter(Boolean)
      .join('\n')}\n} from './types';\n\n${apiFunctionLines.join('\n')}\n${hookFunctionLines.join('\n')}\n${
      apiFunctionLines.length === 0
        ? 'export const generatedApi = {} as const;\n'
        : `export const generatedApi = {\n${contracts
            .map((contract) => {
              const endpointName = toEndpointName(contract.name);
              const fnName = `call${endpointName.charAt(0).toUpperCase()}${endpointName.slice(1)}`;
              return `  ${endpointName}: ${fnName},`;
            })
            .join('\n')}\n} as const;\n`
    }`
  );

  await writeFile(
    path.join(generatedDir, 'runtime.ts'),
    `${generatedTsHeader}import { customFetch, type CustomFetchOptions } from '../../../integrations/custom-fetch';\nimport { invokeSupabaseFunction } from '../../../integrations/supabase/functions';\nimport type { WorkspaceSupabaseClient } from '../../../integrations/supabase/client';\n\nexport interface GeneratedFunctionInvokeInit {\n  headers?: HeadersInit;\n  region?: string;\n}\n\nexport interface GeneratedFunctionRequest<TBody = unknown> {\n  functionName: string;\n  path: string;\n  method: 'GET' | 'POST' | 'PUT' | 'PATCH' | 'DELETE';\n  body?: TBody;\n  init?: GeneratedFunctionInvokeInit;\n}\n\nlet generatedSupabaseClient: WorkspaceSupabaseClient | null = null;\n\nfunction toHeaderRecord(headers?: HeadersInit): Record<string, string> | undefined {\n  if (!headers) return undefined;\n  return Object.fromEntries(new Headers(headers).entries());\n}\n\nexport function setGeneratedSupabaseClient(client: WorkspaceSupabaseClient | null): void {\n  generatedSupabaseClient = client;\n}\n\nexport async function invokeGeneratedFunction<TResponse, TBody = unknown>({\n  functionName,\n  path,\n  method,\n  body,\n  init,\n}: GeneratedFunctionRequest<TBody>): Promise<TResponse> {\n  if (generatedSupabaseClient) {\n    const invokeOptions: Parameters<typeof invokeSupabaseFunction>[2] = {\n      body: body as Parameters<typeof invokeSupabaseFunction>[2] extends { body?: infer TInvokeBody } ? TInvokeBody : never,\n      method,\n    };\n\n    const headers = toHeaderRecord(init?.headers);\n    if (headers) {\n      invokeOptions.headers = headers;\n    }\n    if (init?.region) {\n      invokeOptions.region = init.region as Parameters<typeof invokeSupabaseFunction>[2] extends { region?: infer TRegion } ? TRegion : never;\n    }\n\n    return invokeSupabaseFunction<TResponse>(generatedSupabaseClient, functionName, invokeOptions);\n  }\n\n  const requestInit: CustomFetchOptions = {\n    method,\n    responseType: 'json',\n    headers: toHeaderRecord(init?.headers),\n  };\n\n  if (body !== undefined) {\n    requestInit.headers = {\n      'Content-Type': 'application/json',\n      ...(toHeaderRecord(init?.headers) ?? {}),\n    };\n    requestInit.body = JSON.stringify(body);\n  }\n\n  return customFetch<TResponse>(path, requestInit);\n}\n`
  );

  await writeFile(
    path.join(generatedDir, 'index.ts'),
    `${generatedTsHeader}export * from './endpoints';\nexport * from './types';\nexport * from './runtime';\nexport * from './api';\n`
  );
}

main().catch((error) => {
  console.error('[gen:supabase-api] failed:', error);
  process.exitCode = 1;
});
